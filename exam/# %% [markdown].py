# ✏️ 과제 3 풀이
# 시나리오 A vs B 시뮬레이션 + 라인 차트 + 결론 print()

months = [1, 2, 3, 4, 5, 6]
start_customers = 3200
new_customers = 120

# 계산 함수

def simulate_customers(start, churn_rate, new_monthly, months):
    customers = start
    history = []
    for _ in months:
        churn = customers * churn_rate
        customers = customers - churn + new_monthly
        history.append(customers)
    return history

scenario_a = simulate_customers(start_customers, 0.28, new_customers, months)
scenario_b = simulate_customers(start_customers, 0.20, new_customers, months)

fig_sim = go.Figure()
fig_sim.add_trace(go.Scatter(
    x=months,
    y=scenario_a,
    mode='lines+markers',
    name='시나리오 A: 현상 유지 (28%)',
    line=dict(color='black', width=2),
    marker=dict(size=8, color='white', line=dict(color='black', width=2))
))
fig_sim.add_trace(go.Scatter(
    x=months,
    y=scenario_b,
    mode='lines+markers',
    name='시나리오 B: 배송 개선 (20%)',
    line=dict(color='gray', width=2, dash='dash'),
    marker=dict(size=8, color='white', line=dict(color='black', width=2))
))

fig_sim.update_layout(
    title='6개월 후 구독자 수 시나리오 비교',
    xaxis_title='개월',
    yaxis_title='구독자 수',
    template='plotly_white',
    font=dict(color='black'),
    hovermode='x unified'
)
fig_sim.show()

final_a = scenario_a[-1]
final_b = scenario_b[-1]
difference = final_b - final_a

print(f"시나리오 A 6개월 후 구독자 수: {int(final_a)}명")
print(f"시나리오 B 6개월 후 구독자 수: {int(final_b)}명")
print(f"6개월 후 시나리오 B가 시나리오 A보다 {int(difference)}명 더 유지됩니다.")

